fun main() {
    var inicioContagem = 10
    var finalContagem = 0

    while(inicioContagem>finalContagem)
    {
      println("Contagem em ordem decrescente: $inicioContagem")

        inicioContagem--

        if(inicioContagem==finalContagem)
        {
            println("Fim da contagem :)")
        }
    }
}
